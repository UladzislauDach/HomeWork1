public class Raschet {

    //Расчёт суммы
    public double calculateSum(double a, double b) {
        double result = a + b;
        return result;
    }

    public double calculateVic(double a, double b) {
        double result = a - b;
        return result;
    }

    public double calculateYmn(double a, double b) {
        double result = a * b;
        return result;
    }

    public double calculateDel(double a, double b) {
        double result = a / b;
        return result;
    }

}